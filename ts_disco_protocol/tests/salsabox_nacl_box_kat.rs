//! Cross-implementation known-answer test (KAT) for NaCl `box` (X25519 +
//! XSalsa20-Poly1305), the AEAD construction `crypto_box::SalsaBox` provides.
//!
//! This is the one crypto primitive the engine relied on with **no Go
//! cross-validation** — disco (`ts_disco_protocol`, this crate) seals its
//! NAT-traversal messages with `crypto_box::SalsaBox::new(&their_pub, &my_secret)`
//! over a 24-byte nonce (see `packet.rs::encrypt_in_place` / `decrypt`), and the
//! DERP client/server handshake (`ts_derp::client`) seals its client-info /
//! server-info with the identical construction. Go's `disco` and DERP both use
//! `golang.org/x/crypto/nacl/box`. A divergence in the X25519→HSalsa20 key
//! derivation, the XSalsa20 keystream, or the Poly1305 MAC would let two Rust
//! nodes interoperate with each other while silently failing against **every** Go
//! peer and every Go DERP server — a total connectivity break invisible to a
//! self-round-trip test. This KAT pins the wire bytes in both directions.
//!
//! Provenance: the reference `sealed` bytes come from Go `nacl/box.Seal` over
//! fixed X25519 keypairs + fixed 24-byte nonces + fixed plaintexts, generated by
//! `tests/vectors/gen/nacl_box/main.go` (Go module pinned `golang.org/x/crypto
//! v0.52.0`) and recorded in `tests/vectors/nacl_box_kat.json`. The vectors are
//! embedded inline below (mirroring `blake2s_kat`/the WG handshake KAT, which
//! likewise inline their reference values). `box.Seal` returns the NaCl wire
//! layout `16-byte Poly1305 tag || ciphertext` — the authenticator PRECEDES the
//! ciphertext (the standard `crypto_secretbox`/NaCl convention, and exactly why
//! the disco header carries the tag in a dedicated field rather than appended).
//! `SalsaBox::encrypt_in_place_detached` returns the ciphertext in place with the
//! tag detached, so `tag || ct` must equal Go's `sealed`.

use crypto_box::{
    PublicKey, SalsaBox, SecretKey, Tag,
    aead::{AeadInPlace, generic_array::GenericArray},
};

/// One NaCl-box vector: a sender seals `pt` to the recipient under `nonce`.
struct Vector {
    desc: &'static str,
    sender_priv: &'static str,
    recipient_priv: &'static str,
    nonce: &'static str,
    pt: &'static str,
    /// Go `nacl/box.Seal` output: `16-byte Poly1305 tag || ciphertext` (NaCl layout).
    sealed: &'static str,
}

