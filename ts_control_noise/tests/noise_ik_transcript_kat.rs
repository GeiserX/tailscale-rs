//! Cross-implementation known-answer test (KAT) for the control-plane Noise_IK
//! handshake transcript.
//!
//! `ts_control_noise::Handshake::initialize` drives a `noise_protocol` IK handshake
//! over `<X25519, ChaCha20Poly1305BigEndian, Blake2s>` (noise-rust-crypto) — the exact
//! construction Tailscale's `control/controlbase` uses on the wire: protocol name
//! `Noise_IK_25519_ChaChaPoly_BLAKE2s`, both handshake payloads EMPTY, and the client
//! (initiator) takes `tx = c1 (i->r)` / `rx = c2 (r->i)` from `Split()`.
//!
//! Only the AEAD leaf (the big-endian transport nonce) was previously Go-pinned
//! (`be_aead_matches_go_kat` in `cipher.rs`). The IK TRANSCRIPT itself — the DH
//! schedule, the MixHash/MixKey ordering, prologue absorption, and the `Split` ->
//! `(tx, rx)` DIRECTION — was not. A divergence there would let two Rust nodes
//! interoperate while silently failing against every Go control server: a total
//! registration break invisible to a self-round-trip test. The most dangerous variant
//! is a `tx`/`rx` swap — it passes a Rust-vs-Rust round-trip (both sides swapped agree)
//! yet breaks real registration, where one party is Go.
//!
//! Provenance: the reference bytes come from an INDEPENDENT Go Noise implementation
//! (`github.com/flynn/noise` v1.1.0, primitives from `golang.org/x/crypto` v0.52.0)
//! driven with FIXED static + ephemeral keypairs, generated by
//! `tests/vectors/gen/control_noise/main.go` and recorded in
//! `tests/vectors/control_noise_ik_transcript.json`. The vectors are embedded inline
//! below (mirroring `blake2s_kat` / `be_aead_matches_go_kat` / the WG handshake KAT,
//! which likewise inline their reference values).
//!
//! The KAT pins the transcript MECHANICS with a FIXED prologue constant
//! (`PROLOGUE_ASCII` below). That is deliberately independent of the production
//! `CapabilityVersion::CURRENT` wiring — the prologue string is whatever the caller
//! passes to `Handshake::initialize`; this KAT hardcodes one fixed value, byte-identical
//! to the generator, so the transcript is reproducible.
//!
//! These tests drive the REAL production builder path (`noise_protocol`'s
//! `HandshakeStateBuilder` configured exactly as `Handshake::initialize` does) with a
//! test-only fixed-ephemeral seam (`builder.set_e`), so what is pinned is the production
//! code's transcript, not a parallel re-implementation.

use noise_protocol::{
    Cipher as _, CipherState, DH, HandshakeState, HandshakeStateBuilder, patterns::noise_ik,
};
use noise_rust_crypto::{Blake2s, X25519, sensitive::Sensitive};
use ts_control_noise::ChaCha20Poly1305BigEndian;
use zeroize::Zeroizing;

type Cipher = ChaCha20Poly1305BigEndian;

