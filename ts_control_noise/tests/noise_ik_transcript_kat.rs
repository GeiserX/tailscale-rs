// ----- helpers -----

fn unhex(s: &str) -> Vec<u8> {
    assert!(
        s.len().is_multiple_of(2),
        "hex string must have even length"
    );
    (0..s.len() / 2)
        .map(|i| u8::from_str_radix(&s[i * 2..i * 2 + 2], 16).expect("valid hex byte"))
        .collect()
}

fn hex(bytes: &[u8]) -> String {
    let mut s = String::with_capacity(bytes.len() * 2);
    for b in bytes {
        s.push_str(&format!("{b:02x}"));
    }
    s
}

fn key32(s: &str) -> [u8; 32] {
    unhex(s).try_into().expect("32-byte key")
}

/// Build the noise-rust-crypto X25519 private-key type exactly as `Handshake::initialize`
/// does for the static key (`Sensitive::from(Zeroizing::from(bytes))`).
fn sensitive_key(bytes: [u8; 32]) -> Sensitive<[u8; 32]> {
    Sensitive::from(Zeroizing::from(bytes))
}

/// Construct the initiator handshake state EXACTLY as `Handshake::initialize` does
/// (`noise_ik`, `is_initiator = true`, `set_rs(resp_static_pub)`, `set_prologue(...)`,
/// `set_s(init_static_priv)`), PLUS the test-only fixed ephemeral seam `set_e`.
fn build_initiator() -> HandshakeState<X25519, Cipher, Blake2s> {
    let prologue = PROLOGUE_ASCII.as_bytes();
    let mut builder = HandshakeStateBuilder::<X25519>::new();
    builder.set_pattern(noise_ik());
    builder.set_is_initiator(true);
    builder.set_rs(key32(RESP_STATIC_PUB));
    builder.set_prologue(prologue);
    builder.set_s(sensitive_key(key32(INIT_STATIC_PRIV)));
    builder.set_e(sensitive_key(key32(INIT_EPHEM_PRIV)));
    builder.build_handshake_state()
}

/// Construct the responder handshake state the way the real control server would: IK
/// responder does NOT pre-set `rs` (it learns the initiator static from the encrypted
/// `s` token inside msg1). Fixed static + fixed ephemeral injected.
fn build_responder() -> HandshakeState<X25519, Cipher, Blake2s> {
    let prologue = PROLOGUE_ASCII.as_bytes();
    let mut builder = HandshakeStateBuilder::<X25519>::new();
    builder.set_pattern(noise_ik());
    builder.set_is_initiator(false);
    builder.set_prologue(prologue);
    builder.set_s(sensitive_key(key32(RESP_STATIC_PRIV)));
    builder.set_e(sensitive_key(key32(RESP_EPHEM_PRIV)));
    builder.build_handshake_state()
}

/// Seal `pt` under a freshly-`Split` `CipherState` at counter 0, empty AD. counter 0 is
/// identical under little-endian (Go flynn/noise) and big-endian (the fork's transport
/// AEAD) nonce packing, so this is a valid cross-implementation check.
fn seal_at_zero(cs: &mut CipherState<Cipher>, pt: &[u8]) -> Vec<u8> {
    cs.encrypt_vec(pt)
}

// ----- tests -----

/// The full Go-pinned transcript: build the REAL production initiator (with fixed
/// ephemeral), assert msg1 byte-for-byte, feed Go's msg2, then assert the split ciphers
/// match Go's — INCLUDING DIRECTION (`tx` = i->r, `rx` = r->i). The direction assertion
/// is the load-bearing one: a `tx`/`rx` swap survives a round-trip but breaks real
/// registration against a Go peer.
#[test]
fn noise_ik_transcript_matches_go_kat() {
    // Sanity: the construction name is the Tailscale control protocol name. The fork's
    // cipher reports "ChaChaPoly", matching flynn/noise's cipher name component.
    assert_eq!(
        Cipher::name(),
        "ChaChaPoly",
        "cipher name component of the Noise protocol name diverged"
    );

    let mut state = build_initiator();

    // msg1: write_message with an EMPTY payload, into a fixed-size 96-byte buffer (the
    // production `Initiation::PAYLOAD_LEN`). Compare the raw Noise body to Go's msg1.
    let mut msg1 = [0u8; 96];
    state
        .write_message(&[], &mut msg1)
        .expect("initiation write_message must fit PAYLOAD_LEN");
    assert_eq!(
        hex(&msg1),
        MSG1_HEX,
        "Noise IK msg1 body diverges from the Go reference (DH schedule / MixHash / \
         prologue absorption mismatch)"
    );

    // msg2: read Go's responder message. Must yield an EMPTY payload and complete.
    let payload = state
        .read_message_vec(&unhex(MSG2_HEX))
        .expect("must read Go's msg2");
    assert!(payload.is_empty(), "msg2 payload must be empty");
    assert!(state.completed(), "handshake must be completed after msg2");

    // Split. noise_protocol returns `(tx, rx)` in Noise spec order for the initiator:
    // tx = c1 = initiator->responder, rx = c2 = responder->initiator.
    let (mut tx, mut rx) = state.get_ciphers();

    let probe = PROBE_ASCII.as_bytes();

    // THE LOAD-BEARING DIRECTION ASSERTIONS.
    let tx_sealed = seal_at_zero(&mut tx, probe);
    assert_eq!(
        hex(&tx_sealed),
        TX_PROBE_SEALED_HEX,
        "tx must be the initiator->responder cipher (Go csI0). A mismatch here means the \
         split-key derivation or the tx/rx DIRECTION diverges from Go — a tx/rx swap \
         passes a round-trip but breaks real control registration."
    );
    let rx_sealed = seal_at_zero(&mut rx, probe);
    assert_eq!(
        hex(&rx_sealed),
        RX_PROBE_SEALED_HEX,
        "rx must be the responder->initiator cipher (Go csI1)"
    );
}

/// Guard that the fixed inputs really are the ones Go generated the vectors from: the
/// X25519 base-point public keys derived from the fixed private scalars must match Go's
/// emitted public keys. (Also documents the handshake-hash pin for cross-reference.)
#[test]
fn fixed_public_keys_match_go() {
    let cases = [
        (INIT_STATIC_PRIV, INIT_STATIC_PUB, "init static"),
        (RESP_STATIC_PRIV, RESP_STATIC_PUB, "resp static"),
        (INIT_EPHEM_PRIV, INIT_EPHEM_PUB, "init ephemeral"),
        (RESP_EPHEM_PRIV, RESP_EPHEM_PUB, "resp ephemeral"),
    ];
    for (priv_hex, pub_hex, label) in cases {
        // Derive via the exact production X25519 DH path (noise-rust-crypto), not a
        // separate dalek dependency: this is the same code the real handshake uses.
        let pk = <X25519 as DH>::pubkey(&sensitive_key(key32(priv_hex)));
        assert_eq!(
            hex(&pk),
            pub_hex,
            "{label} X25519 base-point public key must match the Go vector"
        );
    }
    // Document that the handshake-hash constant is a fixed 32-byte value (channel binding).
    assert_eq!(
        unhex(HANDSHAKE_HASH_HEX).len(),
        32,
        "handshake hash must be 32 bytes (BLAKE2s-256)"
    );
}

/// Rust-vs-Rust self-consistency (Option B supplement): run the full IK transcript with
/// fixed ephemerals on BOTH sides entirely in Rust, then assert the two parties derive
/// matching, correctly-directioned split ciphers — the initiator's `tx` (i->r) is opened
/// by the responder's first cipher, and the responder's i->r cipher round-trips to the
/// initiator's `rx`. This guards the wiring even without the Go toolchain, and
/// independently re-derives the direction the Go KAT pins.
#[test]
fn noise_ik_transcript_rust_self_consistent() {
    let mut initiator = build_initiator();
    let mut responder = build_responder();

    // msg1 i->r.
    let mut msg1 = [0u8; 96];
    initiator
        .write_message(&[], &mut msg1)
        .expect("initiator msg1");
    let r_payload = responder
        .read_message_vec(&msg1)
        .expect("responder reads msg1");
    assert!(r_payload.is_empty(), "msg1 payload empty");

    // msg2 r->i.
    let msg2 = responder.write_message_vec(&[]).expect("responder msg2");
    let i_payload = initiator
        .read_message_vec(&msg2)
        .expect("initiator reads msg2");
    assert!(i_payload.is_empty(), "msg2 payload empty");

    assert!(
        initiator.completed() && responder.completed(),
        "both completed"
    );

    // Both parties split. Spec order on each side is (i->r, r->i).
    let (mut i_tx, mut i_rx) = initiator.get_ciphers(); // (i->r, r->i)
    let (mut r_tx, mut r_rx) = responder.get_ciphers(); // (i->r, r->i)

    // The two implementations must agree on the IK transcript: identical raw msg bytes
    // here AND below the matching split-cipher directions.
    assert_eq!(
        hex(&msg1),
        MSG1_HEX,
        "Rust-vs-Rust msg1 must equal the pinned Go msg1"
    );

    let probe = PROBE_ASCII.as_bytes();

    // Initiator's tx (i->r) must be opened by the responder's RECEIVE cipher for i->r,
    // which on the responder is its first split cipher (also labeled i->r). Seal with the
    // initiator's tx, open with the responder's i->r cipher.
    let tx_sealed = i_tx.encrypt_vec(probe);
    let opened = r_tx
        .decrypt_vec(&tx_sealed)
        .expect("responder i->r cipher must open initiator tx");
    assert_eq!(opened, probe, "i->r round-trip plaintext mismatch");

    // Symmetrically, responder's r->i cipher seals to the initiator's rx.
    let rx_sealed = r_rx.encrypt_vec(probe);
    let opened2 = i_rx
        .decrypt_vec(&rx_sealed)
        .expect("initiator rx cipher must open responder r->i");
    assert_eq!(opened2, probe, "r->i round-trip plaintext mismatch");
}
